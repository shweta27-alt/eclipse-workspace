package Hashset;

public class Student {
	String Name;
	String ID;
	public Student(String name, String iD) {
		Name = name;
		ID = iD;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	
	

}
